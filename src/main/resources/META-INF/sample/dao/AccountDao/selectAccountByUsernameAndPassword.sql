SELECT
  SIGNON.USERNAME,
  ACCOUNT.EMAIL,
  ACCOUNT.FIRSTNAME,
  ACCOUNT.LASTNAME,
  ACCOUNT.STATUS,
  ACCOUNT.ADDR1,
  ACCOUNT.ADDR2,
  ACCOUNT.CITY,
  ACCOUNT.STATE,
  ACCOUNT.ZIP,
  ACCOUNT.COUNTRY,
  ACCOUNT.PHONE,
  PROFILE.LANGPREF,
  PROFILE.FAVCATEGORY,
  PROFILE.MYLISTOPT,
  PROFILE.BANNEROPT,
  BANNERDATA.BANNERNAME
FROM 
  ACCOUNT, 
  PROFILE, 
  SIGNON, 
  BANNERDATA
WHERE 
  ACCOUNT.USERID = /*username*/'j2ee'
  AND 
  SIGNON.PASSWORD = /*password*/'j2ee'
  AND 
  SIGNON.USERNAME = ACCOUNT.USERID
  AND 
  PROFILE.USERID = ACCOUNT.USERID
  AND 
  PROFILE.FAVCATEGORY = BANNERDATA.FAVCATEGORY